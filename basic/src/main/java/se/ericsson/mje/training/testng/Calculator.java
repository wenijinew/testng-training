package se.ericsson.mje.training.testng;


/**
 * Calculator interface for basic arithmetic.
 */
public interface Calculator {

    /**
     * @param a
     * @param b
     * @return - sum of a and b
     */
    int add(int a, int b);

    /**
     * @param a
     * @param b
     * @return - sum of a and b
     */
    int minus(int a, int b);
}
